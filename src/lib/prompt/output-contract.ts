export function buildOutputContractTail(designSystem: "flat" | "liquid"): string {
  const label = designSystem === "liquid" ? "リキッド（半透明ガラス）" : "フラット（不透明トーン）";
  return `\n---\n\n上に書かれた仕様すべてを内包する一枚のHTMLを書いてください。デザインテイストは「${label}」です。`;
}
